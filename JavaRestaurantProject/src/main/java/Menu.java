public class Menu {
    private FoodItem starter;
    private FoodItem mainCourse;
    private FoodItem dessert;
    private FoodItem drink;

    public FoodItem getStarter() {
        return starter;
    }

    public void setStarter(FoodItem starter) {
        this.starter = starter;
    }

    public FoodItem getMainCourse() {
        return mainCourse;
    }

    public void setMainCourse(FoodItem mainCourse) {
        this.mainCourse = mainCourse;
    }

    public FoodItem getDessert() {
        return dessert;
    }

    public void setDessert(FoodItem dessert) {
        this.dessert = dessert;
    }

    public FoodItem getDrink() {
        return drink;
    }

    public void setDrink(FoodItem drink) {
        this.drink = drink;
    }
}
